from aurum_core.llm_agent import call_llm

def identify_prescription(
    prescription_text: str,
    api_key: str,
    model: str = "deepseek-v4-pro"
) -> str:
    """
    根据药方文本（可能是方名或具体药物组成），识别标准方剂名。
    支持多个方剂（如“归脾汤合逍遥散”）。
    如果无法识别，返回“无法识别”。
    """
    system_prompt = """你是一位资深中医临床药师。请根据提供的药方内容，按以下流程思考，准确识别其对应的标准方剂名称。
    ## 思考流程
        1. **药物分析**：根据药方组成、剂量、剂量比例，匹配经典方剂，选择最有可能的一个或几个方。
        2. **方剂匹配**：结合加减药物，推断最终方剂名。
        3. **方解**：分析君臣佐使或药味聚类等配伍结构，写出每味药的在方中的效用。
    ## 输出格式（严格遵守，并写出“【方剂名称】”“【方解】”以示分段）
        【方剂名称】
            （只输出方剂名称，如“归脾汤加减”，多个用顿号分隔。不加方名以外的文字）
        【方解】
            （写出该方的配伍结构、药味效用、剂量考量、角药对药（如有），100-200字）
    ## 规则：
        1. 若已是明确方名，直接返回该方名。若为多方的合方，返回所有方名，用中文顿号“、”分隔。
        2. 若为经典方加减，返回“xx加减方”。
        3. 单味药剂量和多味药间剂量比例是识别要点之一。
        4. 若无法准确推断，返回“无法识别”，方解写“无”。不杜撰方名。
        5. 方解语言以现代书面语为主，必须使用中医药术语，不失专业性和严谨性。
        """
    user_prompt = f"药方内容：{prescription_text}"
    messages = [
        {"role": "system", "content": system_prompt},
        {"role": "user", "content": user_prompt}
    ]
    return call_llm(messages, api_key, model)

def identify_diagnosis(
    medical_text: str,
    api_key: str,
    model: str = "deepseek-v4-pro",
    mode: str = "中医诊断"  # "中医诊断" 或 "西医诊断"
) -> str:
    if mode == "中医诊断":
        system_prompt = """你是一位资深中医全科主任医师。请根据患者的病历，按下列流程思考，最后给出准确的中医诊断（证型）。
            ## 推理步骤（严格按以下顺序思考）
            **1.抓主症**
                从患者主诉、伴随症状、舌象、脉象中提取关键信息，简明概括核心症状群。
            **2.定辨证体系与辨病性**
                判断病性的寒/热、虚/实、表/里。根据病情特点，选择最适合的辨证框架，如：六经辨证、卫气营血辨证、脏腑辨证、气血津液辨证或经络辨证。
            **3.定病位**
                在所选框架下，确定病变位置。
            **4.综合辨证**
                结合病性、病位，归纳出完整的证型名称。
            ## 输出格式（严格遵守，并写出“【推理过程】”“【最终结论】”以示分段）：
                【推理过程】
                （逐条写出1-4步的分析）
                【最终结论】
                （推理得出的证型名称）
            ## 输出规则
            1. 推理过程需简洁明了，每步不超过2-3句话。
            2. 如果存在多个证型，用中文顿号“、”分隔。
            3. 如果信息不足以做出明确诊断，返回“无法识别”。
            4. 优先使用经典或统编教材中的证型名称，不杜撰。
            """
    else:  # 西医诊断
        system_prompt = """你是一位资深西医临床医师，请按照规范的临床诊断流程，根据患者病历给出准确的西医诊断（疾病名称）。

            ## 推理步骤（严格按以下顺序思考）
            **1. 系统定位**
            根据病历中描述的主要症状、体征，判断最可能涉及的器官系统。
            
            **2. 症状分析**
            针对定位的系统，分析病历中提及的与该系统相关的典型症状，结合这些信息缩小可能的疾病范围。
            
            **3. 体格检查结果**
            分析病历中提及的体格检查结果，特别关注阳性体征，用以排除或支持某些疾病。
            
            **4. 实验室及辅助检查结果**
            分析病历中的实验室检查结果，这些结果是诊断的重要依据。
            
            **5. 综合确诊**
            结合以上所有信息，给出最可能的西医诊断名称。
            
            ## 输出格式（严格遵守，并写出“【推理过程】”“【最终结论】”以示分段）：
            【推理过程】
            （逐条写出1-5步的分析，每步1-2句话）
            【最终结论】
            （推理得出的西医诊断名称，如“2型糖尿病”，多个用顿号分隔）
            
            ## 输出规则
            1. 如果病历信息不足以完成某一步骤，在该步注明“信息不足”。
            2. 如果存在多个诊断，用中文顿号“、”分隔。
            3. 如果最终无法确诊，返回“无法明确诊断”。
            4. 使用规范的ICD疾病名称或临床常用病名。
            """
    user_prompt = f"病历描述：{medical_text}"
    messages = [
        {"role": "system", "content": system_prompt},
        {"role": "user", "content": user_prompt}
    ]
    return call_llm(messages, api_key, model)


def generate_commentary(
        combined_text: str,
        api_key: str,
        model: str = "deepseek-v4-pro",
        timeout: int = 120,
        style: str = "student"  # "student" 或 "expert"
) -> str:
    """
    根据合并后的病历文本生成按语
    style: "student" 或 "expert"
    """
    if style == "expert":
        system_prompt = """你是一位资深中医全科主任医师，请按以下步骤逐步推理，生成按语。

        ## 推理路径
        **1.抓主证**
        提炼核心症状群及各诊次变化的关键症状
        **2.辨病机**
        分析核心病机，其在每次诊疗中如何演变。分辨相应病机用到的辩证方法（如六经辨证、脏腑辩证等）。
        **3.析治法**
        根据病机演变，分析各阶段治则治法的变化。
        **4.解方药**
        解释关键药物选择的意图，以及各阶段方药调整的思路。
        **5.成按语**
        基于以上推理，生成一篇连贯、精炼、有学术深度的按语。

        ## 按语写作要求
        1. **严格按时间线叙事**：按语应呈现时序的诊疗演变过程，体现病机与治法的动态调整，严禁将后期症状写入前期诊次。
        2. **结合经典**：在辨证思路中恰当引用与病机、辩证方法对应的经典原文，如《金匮要略》《温病条辨》《医宗金鉴》等，以佐证病机判断和用药思路。若方药与经典关联，只能用药性反推来解释经典。
        3. **语言精炼，学术化**：用语准确、凝练，体现学术深度，专业但不晦涩。适合文献汇报或专业期刊。
        4. **结构建议**：首段总括核心病机与治则，中段按时间线分析各诊次的辨证要点与用药调整，末段总结病势转归与预后，点明治疗关键
        5. **篇幅**：只输出按语，控制在400-600字之间，精炼为要。

        ## 按语示例风格参考
        【示例片段】
        患者以胸闷痛、气短为主症，伴头重如裹、大便黏腻，舌紫暗，有瘀斑，苔白腻，脉弦滑，四诊合参属痰瘀互结、胸阳不振之胸痹。首诊方用瓜蒌薤白半夏汤合芪红方加减，方中黄芪补气行血，红景天益气活血通脉，二者为沈教授常用药对，取其“气行则血行，气足则脉通”之意；桂枝温通心阳；丹参活血化瘀；瓜蒌涤痰散结，薤白通阳宣痹，半夏燥湿化痰，三药相须豁痰开结、宣通胸阳。二诊胸痛、胸闷减轻乃痰瘀渐化之象，舌暗红、苔转薄腻可见瘀血渐散，但痰浊未净，故加制远志以化痰安神。此方使痰浊得化、瘀血得行、胸阳得振，契合“病痰饮者当以温药和之”（《金匮要略》）之经旨。
        """
    else:  # student
        system_prompt = """你是一位中医教学经验丰富的老师，请按以下步骤逐步推理，再生成按语。

        ## 推理路径
        **1.抓主证**
        提炼核心症状群及各诊次变化的关键症状。

        **2.辨病性、辨病位、辨病机**
        - 辨病性：判断疾病的寒热虚实属性（如：寒证、热证、虚证、实证）。
        - 辨病位：确定病变所在的脏腑经络（如：病位在脾、在胃、在肝、在肾等）。
        - 辨病机：综合病性与病位，归纳核心病机（如：脾虚湿困、肝郁脾虚等）。
        - 辨证依据：请明确指出哪些症状支持你的辨证判断（如“舌淡胖、脉细弱支持脾虚”），使辨证有理有据。

        **3.析治法**
        根据病机演变，分析各阶段治则治法的变化。

        **4.解方药**
        解释关键药物选择的意图，以及各阶段方药调整的思路。

        **5.成按语**
        基于以上推理，生成一篇用语规范、术语准确、清晰、适合中医专业本科生学习的按语。按语中应明确体现辨证思路，包括病性、病位、病机及其依据。

        ## 按语写作要求
        1. **严格按时间线叙事**：按语应呈现时序的诊疗演变过程，体现病机与治法的动态调整,严禁将后期症状写入前期诊次。
        2. **中医专业性**：必须使用中医核心术语。
        3. **经典引用附带解释**：在辨证思路中恰当引用与病机、辩证方法对应的经典原文，如《金匮要略》《温病条辨》《医宗金鉴》等，引用经典原文时需解释，并且解释的内容必须源自统编教材。若方药与经典关联，只能用药性反推来解释经典。
        4. **语言清晰、逻辑连贯**：避免文言化表达，用现代书面语叙述，但不失专业性和严谨性。
        5. **篇幅**：只输出按语，控制在500-700字之间，便于学生理解。

        ## 按语示例风格参考
        【示例片段】
        患者以胸闷痛、气短为主症，伴头重如裹、大便黏腻，舌紫暗有瘀斑，苔白腻，脉弦滑，综合判断属于“痰瘀互结、胸阳不振”。首诊方用瓜蒌薤白半夏汤合芪红方加减。瓜蒌能化痰散结，薤白能通阳行气，半夏能燥湿化痰，三药合用专门解决痰浊阻塞胸阳的问题；黄芪补气行血，红景天益气活血，二者合用促进气机，气血顺畅则瘀血自然消散。二诊胸痛减轻、舌象变化，说明痰瘀渐化，但尚有痰浊未清，故加远志以化痰安神。此方体现了“化痰与活血并施”的思路，使痰浊得化、瘀血得行，契合《金匮要略》“病痰饮者当以温药和之”之意。
        """

    messages = [
        {"role": "system", "content": system_prompt.format(就诊记录=combined_text)},
        {"role": "user", "content": f"以下是患者的就诊记录汇总：\n\n{combined_text}"}
    ]
    return call_llm(messages, api_key, model, timeout=timeout)

def parse_llm_response(response: str) -> tuple:
    """
    解析LLM返回的【推理过程】和【最终结论】
    返回 (reasoning, conclusion)
    """
    if "【推理过程】" in response and "【最终结论】" in response:
        parts = response.split("【最终结论】")
        reasoning = parts[0].replace("【推理过程】", "").strip()
        conclusion = parts[1].strip()
        return reasoning, conclusion
    else:
        # 如果格式不正确，默认整个作为结论
        return "", response.strip()